package Model;

public class thu {
	private String user;
	private String image;
	public String getUser() {
		return user;
	}
	public void setUser(String user) {
		this.user = user;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public thu(String user, String image) {
		super();
		this.user = user;
		this.image = image;
	}
	public thu() {
		super();
	}
}
